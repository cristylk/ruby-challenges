weather = "sunny"

case weather
	when 'sunny'
		puts "Get out there and enjoy the sun in some shorts!"
	when 'cloudy'
  		puts "Well, at least it's not raining. Better cover up though."
	when 'foggy'
  		puts "Ugh, fog. Bring a hat to deal with that mist."
	when 'rainy'
  		puts "You're gonna need your galoshes."
	else
  		puts "That's some crazy weather. You better just stay in."
end